    private void btnFavoritActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFavoritActionPerformed
        // TODO add your handling code here:
String kota = jTextFieldKota.getText().trim();
    if (kota.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota terlebih dahulu.");
        return;
    }

    if (!kotaFavorit.contains(kota)) {
        kotaFavorit.add(kota);
        saveKotaFavoritToFile();  // Save the updated list to file
        updateKotaFavoritComboBox();
        JOptionPane.showMessageDialog(this, "Kota " + kota + " berhasil ditambahkan ke favorit.");
    } else {
        JOptionPane.showMessageDialog(this, "Kota sudah ada di daftar favorit.");
    }
        
        
    }//GEN-LAST:event_btnFavoritActionPerformed

