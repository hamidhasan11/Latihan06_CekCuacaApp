    private void btnCekCuacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekCuacaActionPerformed
        // TODO add your handling code here:
    String kota = jTextFieldKota.getText().trim();
    if (kota.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota terlebih dahulu.");
        return;
    }
    
    String hasilCuaca = getWeatherData(kota);
    jTextArea1.setText(hasilCuaca);
    
    // Menambahkan data cuaca ke tabel
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.addRow(new Object[]{kota, hasilCuaca.split("\n")[1], hasilCuaca.split("\n")[2]});

    // Menentukan gambar cuaca sesuai dengan kondisi cuaca
    String kondisiCuaca = hasilCuaca.split("\n")[1].toLowerCase();
    String imagePath = "";
    
    if (kondisiCuaca.contains("clear") || kondisiCuaca.contains("sunny")) {
        imagePath = "images/sunny.png"; // Gambar cuaca cerah
    } else if (kondisiCuaca.contains("cloudy") || kondisiCuaca.contains("overcast")) {
        imagePath = "images/cloudy.png"; // Gambar cuaca mendung
    } else if (kondisiCuaca.contains("rain")) {
        imagePath = "images/rainy.png"; // Gambar cuaca hujan
    } else if (kondisiCuaca.contains("snow")) {
        imagePath = "images/snowy.png"; // Gambar cuaca salju
    } else {
        imagePath = "images/default.png"; // Gambar default
    }

    // Menampilkan gambar cuaca di JLabel
    JLabel weatherImageLabel = new JLabel(new ImageIcon(imagePath));
    jPanel1.add(weatherImageLabel); // Menambahkan JLabel ke panel utama
    jPanel1.revalidate();
    jPanel1.repaint();
    }//GEN-LAST:event_btnCekCuacaActionPerformed

