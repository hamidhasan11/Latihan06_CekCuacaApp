    private void saveKotaFavoritToFile() {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter("kotaFavorit.txt"))) {
        for (String kota : kotaFavorit) {
            writer.write(kota);
            writer.newLine();
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}
    
    private void loadKotaFavoritFromFile() {
    kotaFavorit.clear();  // Clear existing data
    try (BufferedReader reader = new BufferedReader(new FileReader("kotaFavorit.txt"))) {
        String line;
        while ((line = reader.readLine()) != null) {
            kotaFavorit.add(line);
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    updateKotaFavoritComboBox();  // Update ComboBox after loading
}
    
    // Fungsi untuk mengambil data cuaca dari API
    private String getWeatherData(String kota) {
        try {
            String urlString = "https://api.openweathermap.org/data/2.5/weather?q=" + kota + "&appid=" + apiKey;
            URL url = new URL(urlString);
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");

            Scanner scanner = new Scanner(url.openStream());
            String response = scanner.useDelimiter("\\A").next();
            scanner.close();

            JSONObject jsonObject = new JSONObject(response);
            String kondisiCuaca = jsonObject.getJSONArray("weather").getJSONObject(0).getString("description");
            double suhu = jsonObject.getJSONObject("main").getDouble("temp") - 273.15; // Konversi dari Kelvin ke Celsius

     
            return "Kota: " + kota + "\nCuaca: " + kondisiCuaca + "\nSuhu: " + String.format("%.2f", suhu) + "Â°C";
        } catch (Exception ex) {
            ex.printStackTrace();
            return "Error: Gagal mendapatkan data cuaca.";
            
        }
        
        
    }

